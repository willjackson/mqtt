 * Defines the MQTT Broker entity.
 *
 * @ingroup mqtt
 *
 * @ContentEntityType(
 *   id = "mqtt_broker",
 *   label = @Translation("Broker"),
 *   handlers = {
 *     "storage" = "Drupal\mqtt\MqttBrokerStorage",
